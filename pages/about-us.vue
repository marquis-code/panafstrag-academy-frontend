<script>
export default {
  scrollToTop: true,
  data() {
    return {
      loading: false,
      form: {
        email: ''
      }
    }
  },
  computed: {
    isFormValid() {
      return !!this.form.email.length
    }
  },
  methods: {
    handleSubscriptionModal() {
      this.$bvModal.show('subscriptionModal')
    },
    closeModal() {
      this.$bvModal.hide('subscriptionModal')
      this.loading = false
    },
    async handleSubscriptionSubmission() {
      this.loading = true
      try {
        await this.$axios.post('https://academy-api-service.onrender.com/api/auth/subscribe', this.form)
        this.$bvModal.hide('subscriptionModal')
      } catch (error) {
        if (error.response) {
          this.$toast.error(error.response.data.errorMessage).goAway(1500)
          this.loading = false
        } else if (error.request) {
          this.loading = false
        } else {
          this.loading = false
        }
      } finally {
        this.loading = false
      }
    }
  }
}
